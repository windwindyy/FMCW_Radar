/**
  SSD1306 128x64 I2C OLED 驱动 (软件I2C)
  PB13=SCL, PB15=SDA
*/
#include "stm32f411xe.h"
#include "oled.h"

#define OLED_ADDR   0x3C

#define SCL_PORT    GPIOB
#define SCL_PIN     (1U << 13)
#define SDA_PORT    GPIOB
#define SDA_PIN     (1U << 15)

#define SCL_H()     (SCL_PORT->BSRR = SCL_PIN)
#define SCL_L()     (SCL_PORT->BSRR = (SCL_PIN << 16))
#define SDA_H()     (SDA_PORT->BSRR = SDA_PIN)
#define SDA_L()     (SDA_PORT->BSRR = (SDA_PIN << 16))
#define SDA_RD()    ((SDA_PORT->IDR & SDA_PIN) != 0)

static uint8_t oled_buffer[OLED_WIDTH * OLED_HEIGHT / 8];

static void i2c_delay(void)
{
    for (volatile int i = 0; i < 40; i++) __NOP();
}

static void I2C_Start(void)
{
    SDA_H(); i2c_delay();
    SCL_H(); i2c_delay();
    SDA_L(); i2c_delay();
    SCL_L(); i2c_delay();
}

static void I2C_Stop(void)
{
    SDA_L(); i2c_delay();
    SCL_H(); i2c_delay();
    SDA_H(); i2c_delay();
}

static uint8_t I2C_WriteByte(uint8_t data)
{
    for (int i = 7; i >= 0; i--) {
        if (data & (1U << i))
            SDA_H();
        else
            SDA_L();
        i2c_delay();
        SCL_H(); i2c_delay();
        SCL_L(); i2c_delay();
    }
    // ACK
    SDA_H(); i2c_delay();
    SCL_H(); i2c_delay();
    uint8_t ack = SDA_RD() ? 0 : 1;
    SCL_L(); i2c_delay();
    return ack;
}

static void OLED_WriteCmd(uint8_t cmd)
{
    I2C_Start();
    I2C_WriteByte(OLED_ADDR << 1);
    I2C_WriteByte(0x00);     // control: command
    I2C_WriteByte(cmd);
    I2C_Stop();
}

static void OLED_WriteData(const uint8_t *data, uint16_t len)
{
    I2C_Start();
    I2C_WriteByte(OLED_ADDR << 1);
    I2C_WriteByte(0x40);     // control: data
    for (uint16_t i = 0; i < len; i++)
        I2C_WriteByte(data[i]);
    I2C_Stop();
}

