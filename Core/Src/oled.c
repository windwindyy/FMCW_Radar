void OLED_Init(void)
{
    // PB13(SCL) / PB15(SDA): open-drain output
    RCC->AHB1ENR |= RCC_AHB1ENR_GPIOBEN;

    GPIOB->MODER   &= ~((3U << 26) | (3U << 30));
    GPIOB->MODER   |=  ((1U << 26) | (1U << 30));  // output
    GPIOB->OTYPER  |=  (SCL_PIN | SDA_PIN);         // open-drain
    GPIOB->OSPEEDR |=  ((3U << 26) | (3U << 30));   // high speed
    GPIOB->PUPDR   &= ~((3U << 26) | (3U << 30));
    GPIOB->PUPDR   |=  ((1U << 26) | (1U << 30));   // pull-up

    SCL_H(); SDA_H();

    // SSD1306 init sequence
    OLED_WriteCmd(0xAE);  // display off
    OLED_WriteCmd(0xD5); OLED_WriteCmd(0x80);
    OLED_WriteCmd(0xA8); OLED_WriteCmd(0x3F);
    OLED_WriteCmd(0xD3); OLED_WriteCmd(0x00);
    OLED_WriteCmd(0x40);
    OLED_WriteCmd(0x8D); OLED_WriteCmd(0x14);
    OLED_WriteCmd(0x20); OLED_WriteCmd(0x00);
    OLED_WriteCmd(0xA1);
    OLED_WriteCmd(0xC8);
    OLED_WriteCmd(0xDA); OLED_WriteCmd(0x12);
    OLED_WriteCmd(0x81); OLED_WriteCmd(0xCF);
    OLED_WriteCmd(0xD9); OLED_WriteCmd(0xF1);
    OLED_WriteCmd(0xDB); OLED_WriteCmd(0x40);
    OLED_WriteCmd(0xA4);
    OLED_WriteCmd(0xA6);
    OLED_WriteCmd(0xAF);  // display on

    OLED_Clear();
    OLED_Update();
}

void OLED_Clear(void)
{
    for (int i = 0; i < sizeof(oled_buffer); i++)
        oled_buffer[i] = 0x00;
}

void OLED_Update(void)
{
    for (uint8_t page = 0; page < OLED_PAGES; page++) {
        OLED_WriteCmd(0xB0 + page);
        OLED_WriteCmd(0x00);
        OLED_WriteCmd(0x10);
        OLED_WriteData(&oled_buffer[page * OLED_WIDTH], OLED_WIDTH);
    }
}

void OLED_ShowString(uint8_t x, uint8_t y, const char *str)
{
    while (*str) {
        if (x > 20) { x = 0; y++; }
        if (y > 7) break;

        uint8_t idx = (*str >= 32 && *str <= 126) ? (*str - 32) : 0;
        uint8_t *buf = &oled_buffer[y * OLED_WIDTH + x * 6];
        for (int col = 0; col < 6; col++)
            buf[col] = font6x8[idx][col];

        x++; str++;
    }
}

void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len)
{
    char buf[11];
    for (int i = len - 1; i >= 0; i--) {
        buf[i] = '0' + (num % 10);
        num /= 10;
    }
    buf[len] = '\0';
    OLED_ShowString(x, y, buf);
}
